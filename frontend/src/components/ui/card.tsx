import * as React from 'react';

import { cn } from '@/lib/utils';

const Card = React.forwardRef<HTMLDivElement, React.HTMLAttributes<HTMLDivElement>>(
  ({ className, style, ...props }, ref) => (
    <div
      ref={ref}
      className={cn('rounded-xl border shadow', className)}
      style={{
        backgroundColor: style?.backgroundColor || 'rgb(var(--color-card))',
        borderColor: style?.borderColor || 'rgb(var(--color-border))',
        color: style?.color || 'rgb(var(--color-card-foreground))',
        ...style,
      }}
      {...props}
    />
  )
);
Card.displayName = 'Card';

